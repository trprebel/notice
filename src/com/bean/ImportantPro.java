package com.bean;

public class ImportantPro {

	private int month;
	private int nextmonth;
	private int nnextmonth;
	private int proid;
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getNextmonth() {
		return nextmonth;
	}
	public void setNextmonth(int nextmonth) {
		this.nextmonth = nextmonth;
	}
	public int getNnextmonth() {
		return nnextmonth;
	}
	public void setNnextmonth(int nnextmonth) {
		this.nnextmonth = nnextmonth;
	}
	public int getProid() {
		return proid;
	}
	public void setProid(int proid) {
		this.proid = proid;
	}

	
}
